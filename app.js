var convertTo2DBoard = function(game) {
    var files = ['a','b','c','d','e','f','g','h'];
    var board = initBoard();
    for(var i = 0; i<8; i++) {
        for (var j = 8 ; j>=1;j--) {
            board[i][8-j] = game.get(files[i]+j);
        }
    }
    return board;
}

var initBoard = function() {
    var board = new Array(8);
    for(var i = 0;i<8;i++ ){
        board[i] = new Array(8);
    }
    return board;
}

